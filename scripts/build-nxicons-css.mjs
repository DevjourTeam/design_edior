import opentype from 'opentype.js'
import { readFileSync, writeFileSync } from 'fs'

const WOFF = 'public/fonts/nxicons.1ec4e9ba.woff'
const OUT = 'src/styles/nxicons.css'

const buf = readFileSync(WOFF)
const ab = buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength)
const b64 = buf.toString('base64')

const font = opentype.parse(ab)
const icons = []
for (let i = 0; i < font.glyphs.length; i++) {
  const g = font.glyphs.get(i)
  if (g.unicode == null || !g.name || g.name === '.notdef') continue
  icons.push({ name: g.name, code: g.unicode.toString(16) })
}
icons.sort((a, b) => a.name.localeCompare(b.name))

const classLines = icons
  .map((i) => `.nxi-${i.name}::before { content: "\\${i.code}"; }`)
  .join('\n')

const css = `/* ============================================================
   nxicons — icon font extracted from the reference editor.
   Auto-generated by scripts/build-nxicons-css.mjs — do not edit by hand.
   Font embedded as a data-URI so it ships inside editor.css (Shopify-safe).
   Usage: <i class="nxi nxi-designs" />
   ============================================================ */

@font-face {
  font-family: "nxicons";
  font-style: normal;
  font-weight: 400;
  font-display: block;
  src: url("data:font/woff;base64,${b64}") format("woff");
}

.nxi,
[class^="nxi-"],
[class*=" nxi-"] {
  font-family: "nxicons" !important;
  font-style: normal !important;
  font-weight: 400 !important;
  font-variant: normal !important;
  text-transform: none !important;
  line-height: 1;
  speak: none;
  display: inline-block;
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}

.nxi::before,
[class^="nxi-"]::before,
[class*=" nxi-"]::before {
  font-family: "nxicons" !important;
}

${classLines}
`

writeFileSync(OUT, css)
console.log(`Wrote ${OUT} with ${icons.length} icons (${(b64.length / 1024).toFixed(1)} KB base64).`)
